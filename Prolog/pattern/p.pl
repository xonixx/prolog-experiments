matches([], []) :-!.
matches([H | T], [H1 | T1]) :-
	(   
	H = H1;
	ischar(H, "?")
	),
	matches(T, T1), !.
matches([H | T], T1) :-
	ischar(H, "*"),
	append(_, T2, T1),
	matches(T, T2), !.
	
	
